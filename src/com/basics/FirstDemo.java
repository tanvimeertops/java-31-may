package com.basics;

public class FirstDemo {
public static void main(String[] args) {
	int i1=2147483647;//4 bytes
	long l1=98989898989l; //8 bytes
	char ch='a';//2 bytes
	byte b1=127;//1 bytes
	float f1=34.56f;//4 bytes
	double d1=456.67;//8 bytes
	boolean bb = false; //1 bit
	
	System.out.println("Integer value is :"+i1);
	System.out.println("Long value is :"+l1);
	System.out.println("char value is :"+ch);
	System.out.println("Byte value is :"+b1);
	System.out.println("float value is :"+f1);
	System.out.println("Bool value is :"+bb);
	System.out.println();
	
}
}
